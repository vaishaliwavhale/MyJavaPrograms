package flowControl;

class SwitchCase5 {
    public static void main (String[]args){
        int Company=3;
        switch (Company){
            case 1:
                System.out.println("TechMahindra");
                break;
            case 2:
                System.out.println("TCS");
                break;
            case 3:
                System.out.println("Infosys");
                break;

        }
    }
}
