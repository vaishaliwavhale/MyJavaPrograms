package flowControl;

 class SwitchCase4 {
     public static void main(String[]args){
         int field=2;
         switch(field){
             case 1:
                 System.out.println("The students having science");
                 break;
             case 2:
                 System.out.println("The students having Art");
                 break;
             case 3:
                 System.out.println("The students having  PCM");
                 break;
             default:
                 System.out.println("You inter invalid choice");
         }
     }
}
