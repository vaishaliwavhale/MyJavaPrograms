package flowControl;

class SwitchCase2 {
    public static void main(String[]args){
        int name=1;
        switch(name){
            case 1:
                System.out.println("Vaishali");
                break;
            case 2:
                System.out.println("Sonal");
                break;
            case 3:
                System.out.println("Komal");
                break;
            case 4:
                System.out.println("Divya");
                break;
        }
    }
}
