package AssignmentOperator;

 class UnaryOperator8 {
     public static void main(String[]args){
         int b=110;
         //Post decrement operator
         System.out.println("Output is:"+ b--);
         System.out.println("Output is:"+ b--);

         //Pre decrement operator
         int a=11;
         System.out.println("Output is:"+ a--);
         System.out.println("Output is:"+ --a);
     }
}
